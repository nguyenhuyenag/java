package common.util;

public class DataTypes {

	public static final byte MIN_BYTE 		=	-128;					//	-2^7
	public static final byte MAX_BYTE 		= 	127; 					//	2^7 - 1
	public static final short MIN_SHORT		= 	-32768; 				//	-2^15
	public static final short MAX_SHORT 	= 	32767; 					//	2^15 - 1
	public static final int MIN_INT 		= 	-2147483648; 			//	-2^31
	public static final int MAX_INT 		= 	2147483647; 			//	2^31 - 1
	public static final long MIN_LONG 		= 	-9223372036854775808l;	//	-2^63
	public static final long MAX_LONG 		= 	9223372036854775807l;	//	2^63 - 1

}
